//PROGRAM TO FIND AVERAGE OF TWO NUMBERS USING INPUT-OUTPUT
.CLEAR
.ORIG X10
.START X10

IN X0 R1
IN X1 R2
IN X2 R3

MOV w R1 R4
ADD w R2 R4
ADD w R3 R4

CALL DIVISION

OUT X3 R6

HALT

DIVISION: MOV W R4 R5
		MOV W X0 R6
	LIMIT: COMP W X3 R5
		JLE OPERATE
		RET
	OPERATE: SUB W X3 R5
		ADD w X1 R6
		JMP LIMIT

.END
		