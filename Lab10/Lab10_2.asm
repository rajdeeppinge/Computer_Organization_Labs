//PROGRAM TO FIND AVERAGE OF TWO NUMBERS USING INPUT-OUTPUT
.ORIG X10
.START X10

MOV b x5 R1

LOOP: IN X0 R2
	IN X1 R3
	IN X2 R4
	IN X3 R5
	COMP b R1 R5
	JNE LOOP

MOV w R2 R6
ADD w R3 R6
ADD w R4 R6

CALL DIVISION

OUT X4 R8

HALT

DIVISION: MOV W R6 R7
		MOV W X0 R8
	LIMIT: COMP W X3 R7
		JLE OPERATE
		RET
	OPERATE: SUB W X3 R7
		ADD w X1 R8
		JMP LIMIT

.END
		